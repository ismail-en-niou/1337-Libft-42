#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	unsigned int	n;
	char			c;

	n = nb;
	if (nb < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	if (n < 10)
	{
		c = n + '0';
		write(fd, &c, 1);
	}
	else
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
}
